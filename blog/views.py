def index(request):
    sorted_posts = sorted(posts,key=get_date)
    latest_posts = sorted_posts[-3:]
    return render(
        request,
        "blog/home-page.html",
    {
        "navigation_url":reverse('blog-listing-page'),
        "navigation_title" : "All Posts",
        "posts":latest_posts
        }
    )
def blog_listing(request):
    return render(
        request,
        "blog/blogs-listing-page.html",
        {
            "navigation_url": reverse('homepage'),
            "navigation_title": "Home",
            "posts": posts
        }
    )
def blog_details(request,slug):
    post = next(post for post in posts if post['slug'] == slug)
    if post is None:
        raise Http404()
    return render(
        request,
        "blog/blogs-details-page.html",
        {
            "slug":slug,
            "navigation_url": reverse('homepage'),
            "navigation_title": "Home",
            "post":post
        }
    )
def get_date(post):
    return post['date']